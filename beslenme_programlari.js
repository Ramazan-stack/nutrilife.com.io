function diyetIcerikGoster(baslik) {
    const mainContent = document.querySelector('.main-content');
    const icerikler = diyetIcerikleri[baslik];
    if (mainContent && icerikler) {
        mainContent.innerHTML = `
            <h1>${baslik}</h1>
            <div class="diyet-kart-listesi">
                ${icerikler.map(item => item.gun !== "Önemli Kurallar" ? `
                    <div class="diyet-karti">
                        <h2>${item.gun}</h2>
                        <img src="${item.foto}" alt="${item.gun}" class="diyet-karti-img">
                        <div class="ogunler">
                            ${item.ogunler.map(ogun => `
                                <div class="ogun">
                                    <strong>${ogun.baslik}:</strong> ${ogun.yemek}
                                </div>
                            `).join('')}
                        </div>
                    </div>
                ` : `
                    <div class="onemli-kurallar-kart">
                        <h2 class="onemli-kurallar-baslik">Önemli Kurallar</h2>
                        <ul class="onemli-kurallar-liste">
                            ${item.kurallar.map(kural => `<li>${kural}</li>`).join('')}
                        </ul>
                    </div>
                `).join('')}
            </div>
        `;
    }
}

document.addEventListener('DOMContentLoaded', function() {
    document.querySelectorAll('.panel a').forEach(function(link) {
        link.addEventListener('click', function(e) {
            const baslik = this.textContent.trim();
            if (diyetIcerikleri[baslik]) {
                e.preventDefault();
                diyetIcerikGoster(baslik);
            }
        });
    });
});